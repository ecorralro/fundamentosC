#include <stdio.h>
int main(int argc,char *argv[]){
    printf("mayor que: %i \n",(5>3));//true=1 por pantalla
    printf("menor que: %i \n",(5<3));//false=0 por pantalla

    printf("mayor o igual que: %i \n",(5>=3));//true
    printf("menor o igual que: %i \n",(5<=3));//false

    printf("igualdad: %i \n",(5==5));//true
    printf("desigualdad: %i \n",(5!=5));//false

    return 0;
}